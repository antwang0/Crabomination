/// Float a tooltip near the cursor when the user holds Alt and hovers
/// a draft card tile. Shows the printed card name — the canonical use
/// case is identifying a tile whose Scryfall art failed to download
/// and renders as the cardback placeholder (so the player can read
/// what the card actually is). Both Pack-tab and Picks-tab tiles
/// carry `DraftCardName`, so this works in both views.
fn update_draft_alt_tooltip(
    keys: Res<ButtonInput<KeyCode>>,
    windows: Query<&Window>,
    hovered: Query<(&Interaction, &DraftCardName)>,
    mut tooltip: Query<(&mut Text, &mut Node), With<DraftAltTooltip>>,
) {
    let Ok((mut text, mut node)) = tooltip.single_mut() else { return };
    let alt_held = keys.pressed(KeyCode::AltLeft) || keys.pressed(KeyCode::AltRight);
    let hover_name = hovered
        .iter()
        .find(|(i, _)| matches!(**i, Interaction::Hovered | Interaction::Pressed))
        .map(|(_, name)| name.0.clone());
    match (alt_held, hover_name) {
        (true, Some(name)) => {
            *text = Text::new(name);
            node.display = Display::Flex;
            if let Ok(window) = windows.single()
                && let Some(cursor) = window.cursor_position()
            {
                // Offset slightly so the tooltip doesn't sit under the
                // cursor and steal hover hits from neighbouring tiles.
                node.left = Val::Px(cursor.x + 14.0);
                node.top = Val::Px(cursor.y + 14.0);
            }
        }
        _ => {
            node.display = Display::None;
        }
    }
}

