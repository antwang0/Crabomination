#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn pack_card_width_grows_as_pack_drains() {
        let win = Vec2::new(1920.0, 1080.0);
        let w15 = compute_pack_card_width(win, 15);
        let w8 = compute_pack_card_width(win, 8);
        let w3 = compute_pack_card_width(win, 3);
        let w1 = compute_pack_card_width(win, 1);
        assert!(w15 <= w8, "8-card pack should not shrink vs 15-card");
        assert!(w8 <= w3, "3-card pack should not shrink vs 8-card");
        assert!(w3 <= w1, "1-card pack should not shrink vs 3-card");
    }

    #[test]
    fn pack_card_width_grows_with_resolution() {
        let small = Vec2::new(1280.0, 720.0);
        let big = Vec2::new(2560.0, 1440.0);
        let w_small = compute_pack_card_width(small, 15);
        let w_big = compute_pack_card_width(big, 15);
        assert!(w_big >= w_small, "wider screen should grant at least as much card width");
    }

    #[test]
    fn pack_card_width_clamped() {
        // A single card on a huge screen should not exceed the max.
        let w = compute_pack_card_width(Vec2::new(4000.0, 4000.0), 1);
        assert!(w <= PACK_CARD_W_MAX);
        // 15 cards on a tiny window stays at the min.
        let w = compute_pack_card_width(Vec2::new(800.0, 600.0), 15);
        assert!(w >= PACK_CARD_W_MIN);
    }
}
